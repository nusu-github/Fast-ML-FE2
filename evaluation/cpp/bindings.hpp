  m.def("_resize_nearest", &resize_nearest, nb::arg("dst"), nb::arg("src"));
  m.def(
      "_build_level_coefficients",
      &build_level_coefficients,
      nb::arg("alpha"),
      nb::arg("eps"),
      nb::arg("omega"),
      nb::arg("neighbor_weights"),
      nb::arg("inv_W"),
      nb::arg("inv_Wp1"),
      nb::arg("fg_gain"),
      nb::arg("bg_gain"));
